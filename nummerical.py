# Переменная для количества выполненных ДЗ
completedAssignments = 12

# Переменная для количества затраченных часов
hoursSpent = 1.5

# Переменная для названия курса
courseName = 'Python'

# Вычисление времени на одно задание ответ 0.125
timePerAssignment = hoursSpent / completedAssignments

# Вывод результатов
print("Количество выполненных ДЗ:", completedAssignments)
print("Количество затраченных часов:", hoursSpent)
print("Название курса:", courseName)
print("Время на одно задание:", timePerAssignment)